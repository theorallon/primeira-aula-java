
public class escolhaa {
    void main() {
        int opcao = 2;

        switch (opcao) {
            case 1:
                System.out.println("Saldo");
                break;
            case 2:
                System.out.println("Extrato");
                break;
            case 3:
                System.out.println("Sair");
                break;
            default:
                System.out.println("Opção inválida");
        }
    }
}